from typing import Dict, List, Optional
from langchain_core.messages import BaseMessage, HumanMessage, SystemMessage


class PurPosePrompts:
    def _build_persona_section(self, persona_info: Optional[Dict]) -> str:
        if not persona_info:
            return ""
        return f"""
        ## 타겟 페르소나 정보 (반드시 반영)
        {persona_info}

        페르소나의 피부 고민, 가치관, 라이프스타일을 메시지 전체에 자연스럽게 반영하세요."""

    def _build_brand_tone_section(self, brand_tone: str) -> str:
        return f"""# 브랜드 톤 가이드
        {brand_tone}

        ## 브랜드톤 활용 원칙
        - 어조(항목 4): 메시지 전체 톤의 기준으로 사용
        - 핵심 키워드(항목 5): 제품과 연관된 키워드 1-2개 반드시 포함
        - 강조 감성(항목 6): 메시지의 감성적 방향 참고
        - 금지 표현(항목 7): 아래 공통 금지 표현에 더해 반드시 준수
        - 문체 예시(항목 8): 문장 구조와 어투 참고"""

    def _build_common_regulations(self) -> str:
        return """## 한국 화장품 광고 규제 및 공통 금지 표현

        ### 화장품법 준수
        - 의약품으로 오인할 수 있는 표현 금지
        - 질병 치료 효능 표현 금지
        - 화장품 기능 범위 내 표현 사용 (보습, 진정, 탄력 케어, 피부톤 정돈, 미백 기능성 해당 시 등)

        ### 절대 사용 금지
        - "치료", "완치", "의학적"
        - "임상 치료", "임상적으로 효과", "의학적으로 입증" (의약품 효능 주장에 활용되는 경우)
        - "주름 제거", "여드름 치료", "아토피 개선"
        - "100% 효과", "영구적", "기적"
        - "No.1", "최고" (객관적 근거 없이)

        ### 허용 표현 (오해 방지)
        - "임상으로 확인된 [보습·장벽 등 화장품 기능]" — 인증 시험 결과 표현은 허용
        - "피부과 테스트 완료", "임상시험 완료" — 안전성 인증 표현은 허용

        ### 권장 대체 표현
        - "피부 결 개선" (○) vs "주름 제거" (✗)
        - "탄력 케어" (○) vs "리프팅 효과" (✗)
        - "수분 공급" (○) vs "건조증 치료" (✗)
        - "피부 진정" (○) vs "염증 치료" (✗)

        브랜드 톤 가이드의 금지 표현(항목 7)도 위와 동일한 수준으로 반드시 준수."""

    def _build_human_prompt(self, product_data: Dict, persona_info: Optional[Dict]) -> str:
        return f"""# 입력 정보
        - 상품정보: {product_data}
        {self._build_persona_section(persona_info)}

        # 출력 형식 (JSON만 출력, 설명 없음)

        {{
            "title": "[40자 이내 제목]",
            "message": "[350자 이내 메시지 본문]"
        }}"""

    def build_purpose_introduction_prompt(
            self,
            product_data: Dict,
            brand_tone: str,
            persona_info: Optional[Dict] = None,
            ) -> List[BaseMessage]:
        system_prompt = f"""당신은 아모레몰의 전문 뷰티 마케팅 카피라이터입니다.
        고객에게 브랜드와 제품을 처음 소개하는 메시지를 작성합니다.

        # 역할 및 목적
        - **목적**: 브랜드/제품 첫 소개
        - **목표**: 신규 고객이 "더 알고 싶다"는 감정을 갖도록 유도

        {self._build_brand_tone_section(brand_tone)}

        # 메시지 작성 원칙

        ## 핵심 강조 포인트
        제품 스펙 나열이 아닌 고객이 얻을 변화와 경험에 집중하세요.
        이 제품만의 차별점 1가지를 중심에 두고, 나머지는 뒷받침 요소로 활용하세요.

        ## 메시지 구성
        다음 3가지를 자연스러운 흐름으로 담으세요 (순서는 메시지 흐름에 맞게 조정):
        ① 고객의 공감을 끄는 진입점 (상황, 감정, 또는 질문)
        ② 이 제품이 다른 것과 다른 핵심 이유 1가지
        ③ 구매나 관심으로 이어지는 소프트 CTA 포함 마무리 (예: "제품 자세히 보기", "지금 살펴보기", "더 알아보기")

        첫 문장은 제품명/브랜드명으로 시작하지 마세요.
        각 요소를 소제목처럼 구분하지 말고 하나의 흐름으로 이어지게 작성하세요.

        ## 작성 제약
        - 제목: 40자 이내, 제품의 핵심 가치나 차별점
        - 메시지 본문: 350자 이내
        - 마지막 문장 또는 별도 한 줄에 소프트 CTA 1개 필수 포함 (예: "제품 자세히 보기", "더 알아보기")
        - 감성적 공감과 구체적 효능을 균형있게 배치
        - 과장하지 않고 검증된 정보만 사용
        - 강압적 표현 금지 ("지금 바로!", "서두르세요!")

        ## 페르소나 맞춤 전략
        - 페르소나의 피부 고민을 진입점으로 연결
        - 페르소나의 가치관(비건, 친환경 등)이 브랜드 가치와 겹치면 언급
        - 페르소나의 연령대와 라이프스타일에 맞는 언어 사용

        ## 피해야 할 요소
        - "최고의", "완벽한", "기적의" 등 과도한 형용사
        - 의학적 효능 과장 또는 허위 표현
        - 전문 용어 나열
        - 강압적 판매 톤

        {self._build_common_regulations()}"""

        human_prompt = self._build_human_prompt(product_data, persona_info)
        return [SystemMessage(content=system_prompt), HumanMessage(content=human_prompt)]

    def build_purpose_new_products_prompt(
            self,
            product_data: Dict,
            brand_tone: str,
            persona_info: Optional[Dict] = None,
            ) -> List[BaseMessage]:
        system_prompt = f"""당신은 아모레몰의 전문 뷰티 마케팅 카피라이터입니다.
        신제품 출시를 고객에게 처음 알리고, 기대감과 초기 구매를 유도하는 메시지를 작성합니다.

        # 역할 및 목적
        - **목적**: 신제품 런칭 안내
        - **목표**: "기존 제품과 무엇이 다른가"에 대한 답을 주어 얼리어답터 확보

        {self._build_brand_tone_section(brand_tone)}

        # 메시지 작성 원칙

        ## 핵심 강조 포인트
        이 제품이 새롭고 특별한 이유를 명확하게 전달하세요.
        기존 제품 대비 차별점 또는 새로운 혁신 포인트가 메시지의 중심이어야 합니다.

        ## 메시지 구성
        다음 3가지를 자연스러운 흐름으로 담으세요:
        ① 신제품 출시를 알리는 임팩트 있는 시작 (설렘, 기대감 유발)
        ② 기존과 다른 핵심 차별점 1-2가지
        ③ 첫 구매 또는 사전 알림을 유도하는 마무리

        첫 문장은 제품명/브랜드명으로 시작하지 마세요.
        상품정보에 출시일이 있으면 반드시 포함하세요.

        ## 작성 제약
        - 제목: 40자 이내, "NEW"/"신제품"/"출시" 등 키워드 활용 가능
        - 메시지 본문: 350자 이내
        - 출시 기념 혜택이 상품정보에 있을 때만 언급 (없으면 생략)
        - 과장하지 않고 검증된 신제품 정보만 사용

        ## 페르소나 맞춤 전략
        - 페르소나의 피부 고민이 이 신제품이 해결하는 문제라면 연결
        - 페르소나의 가치관(비건, 친환경 등)이 신제품 콘셉트와 맞으면 부각

        ## 피해야 할 요소
        - "혁명적", "전무후무한", "세상에 없던" 등 과도한 수식
        - 검증되지 않은 신기술 주장
        - 근거 없는 "세계 최초", "업계 유일"
        - 출시 전 제품에 대한 효능 확정 표현

        {self._build_common_regulations()}"""

        human_prompt = self._build_human_prompt(product_data, persona_info)
        return [SystemMessage(content=system_prompt), HumanMessage(content=human_prompt)]

    def build_purpose_bestseller_prompt(
            self,
            product_data: Dict,
            brand_tone: str,
            persona_info: Optional[Dict] = None,
            ) -> List[BaseMessage]:
        system_prompt = f"""당신은 아모레몰의 전문 뷰티 마케팅 카피라이터입니다.
        검증된 베스트셀러 제품을 추천하여 신뢰 기반의 구매를 유도하는 메시지를 작성합니다.

        # 역할 및 목적
        - **목적**: 베스트셀러 추천
        - **목표**: "많은 사람이 선택했고, 이유가 있다"는 확신을 주어 구매 결정 촉진

        {self._build_brand_tone_section(brand_tone)}

        # 메시지 작성 원칙

        ## 핵심 강조 포인트
        사회적 증거(많은 사람의 선택)와 제품 효능을 결합하세요.
        "왜 베스트셀러인지"에 대한 구체적 이유가 메시지 중심이어야 합니다.

        ## 메시지 구성
        다음 3가지를 자연스러운 흐름으로 담으세요:
        ① 베스트셀러 지위를 전달하는 시작
        ② 많은 사람이 선택한 구체적 이유 1-2가지
        ③ 부담 없는 구매 유도 마무리

        첫 문장은 제품명/브랜드명으로 시작하지 마세요.

        ## 수치 데이터 활용 원칙
        상품정보에 실제 수치(판매량, 재구매율, 리뷰 수 등)가 명시된 경우에만 인용하세요.
        수치가 없으면 숫자를 만들지 말고, 제품의 효능과 특징으로 신뢰를 표현하세요.
        - 수치 있을 때: 정확히 인용 ("누적 판매 N만 개", "재구매율 X%")
        - 수치 없을 때: "꾸준히 사랑받는", "오랫동안 선택되는" 등의 표현 사용

        ## 베스트셀러 유형별 강조 방향
        - 스테디셀러형: 지속성과 신뢰 ("3년째 사랑받는", "시간이 증명한")
        - 입소문 히트형: 확산성과 화제성 ("SNS에서 화제", "품절 대란")
        - 재구매 1위형: 만족도와 충성도 ("한 번 쓰면 계속 찾게 되는")

        ## 작성 제약
        - 제목: 40자 이내, 베스트셀러 지위 표현
        - 메시지 본문: 350자 이내
        - 모든 수치는 상품정보에 있는 실제 데이터만 사용

        ## 페르소나 맞춤 전략
        - 페르소나의 피부 고민과 베스트셀러의 주요 효능을 매칭
        - 페르소나와 유사한 타겟의 선택임을 암시 (페르소나 정보에 근거할 때만)
        - 페르소나의 구매 결정 요인(리뷰, 추천 등)을 신뢰 표현에 반영

        ## 피해야 할 요소
        - 검증되지 않은 판매 실적, 임의로 만든 수치
        - 경쟁사 제품 직접 비교 또는 폄하
        - "모든 사람 만족", "부작용 제로" 같은 일반화
        - 근거 없는 "국민 제품", "대한민국 베스트"

        {self._build_common_regulations()}"""

        human_prompt = self._build_human_prompt(product_data, persona_info)
        return [SystemMessage(content=system_prompt), HumanMessage(content=human_prompt)]

    def build_purpose_promotion_and_event_prompt(
            self,
            product_data: Dict,
            brand_tone: str,
            persona_info: Optional[Dict] = None,
            ) -> List[BaseMessage]:
        system_prompt = f"""당신은 아모레몰의 전문 뷰티 마케팅 카피라이터입니다.
        할인 및 이벤트 정보를 전달하여 즉각적인 구매 행동을 유도하는 메시지를 작성합니다.

        # 역할 및 목적
        - **목적**: 할인/이벤트 안내
        - **목표**: 혜택의 구체성과 기한의 긴급성으로 즉각적 구매 결정 유도

        {self._build_brand_tone_section(brand_tone)}

        # 메시지 작성 원칙

        ## 핵심 강조 포인트
        혜택 정보를 가장 먼저, 가장 명확하게 전달하세요.
        할인 중에도 제품의 본질적 가치를 함께 전달해 "싸서 사는 게 아니라 좋아서 사는" 느낌을 주세요.

        ## 메시지 구성
        다음 3가지를 자연스러운 흐름으로 담으세요:
        ① 혜택/이벤트를 임팩트 있게 전달하는 시작
        ② 이 제품을 지금 구매해야 하는 이유 (혜택 + 제품 가치)
        ③ 명확하고 즉각적인 행동 유도 마무리

        첫 문장은 제품명/브랜드명으로 시작하지 마세요.

        ## 혜택 데이터 활용 원칙
        상품정보에 할인율, 증정품, 기간이 명시된 경우에만 구체적 수치를 작성하세요.
        혜택 정보가 없으면 숫자를 만들지 말고, 제품 자체의 가치를 중심으로 작성하세요.
        - 혜택 정보 있을 때: 정확히 인용 (할인율, 증정품명, 기간)
        - 혜택 정보 없을 때: 제품 효능과 브랜드 가치 중심

        ## 이벤트 유형별 강조 방향
        - 정기 세일: 안정적 신뢰 톤, 정기성 강조로 재방문 유도
        - 기념일/브랜드데이: 축제 분위기, 브랜드 스토리 연결
        - 타임딜: 강한 긴급성, 시간대 명시
        - 첫 구매 할인: 환영 느낌, 브랜드 경험 시작 유도

        ## 작성 제약
        - 제목: 40자 이내, 할인율 또는 주요 혜택 명시 (정보 있을 때)
        - 메시지 본문: 350자 이내
        - 긴급성 부여 시 실제 정보만 사용 (기간, 수량 등)
        - 투명성: 할인 조건을 명확히 공개

        ## 페르소나 맞춤 전략
        - 페르소나의 가치관(가성비, 합리적 소비)을 할인 혜택과 연결
        - 페르소나의 구매 패턴(세트 구매, 대용량 선호)에 맞는 혜택 부각

        ## 피해야 할 요소
        - 허위 할인율 (원가 부풀려 할인율 과장)
        - "지금 아니면 영원히 없음" 등 과도한 긴급성
        - 제품 가치 폄하 표현 ("떨이", "재고 처리", "헐값")
        - "역대급", "최저가" (객관적 근거 없을 때)
        - 강압적 구매 유도

        {self._build_common_regulations()}"""

