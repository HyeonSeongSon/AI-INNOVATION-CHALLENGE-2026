import json
from typing import Union


def build_classify_product_type_prompt(persona_info: Union[dict, str]) -> str:
    if isinstance(persona_info, dict):
        persona_str = json.dumps(persona_info, ensure_ascii=False, indent=2)
    else:
        persona_str = str(persona_info)

    prompt = f"""페르소나를 읽고, 이 사람이 찾고 있는 상품의 구체적인 품목명을 하나 결정하세요.

## 결정 순서

1. 니즈에 품목이 직접 명시된 경우 → 그 품목을 그대로 사용
   예) "휴대용 블렌더를 찾고 있다" → "휴대용 블렌더"
   예) "샴푸 세트가 필요하다" → "샴푸 트리트먼트 세트"
   예) "오일 제품은 끈적임 없이 흡수되는 것을 찾는다" → "바디오일"

2. 니즈에 "방법", "도구", "제품" 등 간접 표현만 있는 경우
   → 니즈 전체에서 사용 동작·상황을 읽어 도구 유형을 추론
   예) "손톱으로 긁다가 상처 날까 봐 두피 딥클렌징 방법을 찾는다"
     → 손톱을 대신할 도구 → "두피 스크럽 브러쉬"
   예) "손이 닿지 않는 등 부위를 셀프로 풀 수 있는 도구"
     → 긴 손잡이 마사지 도구 → "롱스틱 마사지기"
   예) "매트리스 진드기·생활균을 집에서 빠르게 없애는 방법"
     → 침구 전용 청소기 → "침구청소기"

3. 제형·포장이 명시된 경우 → 그 제형/포장에 어울리는 품목
   예) "스포이드형 포장, 즉각 흡수, 저자극" → "페이셜 에센스" 또는 "페이셜 오일"
   예) "티백을 서랍에 넣어두고 우려 마신다" → "기능성 티백"
   예) "한 병씩 바로 마시는 샷형" → "이너뷰티 샷"

4. 위 단서가 없으면 페르소나 전체를 종합해 소비자가 검색할 만한 품목명을 결정한다.

## 품목명 작성 규칙
- 2~4어절 이내로 작성한다
- 재질·색상·색호·향·제형 스펙은 포함하지 않는다
  ❌ "포도맛 스틱형 여성용 프로바이오틱스(장·질 건강)" → ✅ "프로바이오틱스"
  ❌ "쿨링 워터리 선플루이드 SPF50+ PA++++ 펌프형" → ✅ "선크림"
  ❌ "오일 컨트롤 세미매트 리퀴드 파운데이션(펌프형, 뉴트럴 23-25호)" → ✅ "리퀴드 파운데이션"
  ❌ "프레스드 파우더 팩트(무향·21호)" → ✅ "파우더 팩트"
  ❌ "저소음 PTC 세라믹 팬히터(리모컨·과열차단)" → ✅ "전기 히터"

## 주의
- 상위 분류어는 쓰지 않는다: "스킨케어", "헤어케어", "이너뷰티" (X)
- 반드시 실제 상품 품목명 수준으로 쓴다.

## 추가 추출 항목

### end_user
페르소나(구매자)와 실제 제품 사용자가 다를 경우에만 명시한다.
- 다른 경우: 아이용 → "유아" / 어린이용 → "어린이" / 반려동물용 → "반려동물"
- 같은 경우(본인이 직접 씀): null

### product_form
페르소나 니즈에 복합 기능 형태가 명시된 경우에만 추출한다.
- 포함: "올인원", "샴푸&바디워시", "2-in-1", "세트" 등
- 명시 없으면: null

출력 형식 (JSON만 출력, 설명 없음)
{{
  "product_type": "품목명",
  "end_user": "실제 사용자 또는 null",
  "product_form": "복합 형태 명칭 또는 null"
}}

Persona:
{persona_str}
"""
    return prompt
