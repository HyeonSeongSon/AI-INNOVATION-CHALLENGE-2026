import json
from typing import Dict, Union


def build_generate_product_search_query_from_persona_prompt(persona_info: Union[dict, str]) -> Dict:
    if isinstance(persona_info, dict):
        persona_str = json.dumps(persona_info, ensure_ascii=False, indent=2)
    else:
        persona_str = str(persona_info)

    prompt = f"""
당신은 뷰티 상품 추천 시스템을 위한 검색 쿼리 생성 전문가입니다.
주어진 페르소나 정보를 분석하여 상품 검색에 사용할 쿼리를 생성하세요.

추천 시스템 구조
1. Retrieval 단계: retrieval_query로 상품 후보를 검색
2. Rerank 단계
   - user_need_query       → product_function_desc 매칭
   - user_preference_query → product_attribute_desc 매칭
   - persona_query         → product_target_user 매칭

페르소나 입력 형식
페르소나는 아래 4개 자연어 필드로 구성됩니다:
- 기본 정보: 나이, 성별, 직업
- 라이프스타일: 직업·생활 리듬에서 비롯된 일과 패턴과 습관
- 니즈: 이 상품을 찾게 된 핵심 고민
- 취향: 형태·맛·성분·가격·포장 등 상품 선택 기준

페르소나 카테고리 판단
니즈·취향 필드를 기준으로 어떤 카테고리 상품을 원하는지 판단하세요:
- 스킨케어: 피부 고민(건조·트러블·주름·미백), 스킨케어 루틴(토너·세럼·크림)
- 색조/메이크업: 메이크업 고민(발색·지속력·커버력), 퍼스널컬러, 베이스 호수
- 메이크업 도구: 메이크업 루틴에 도구(스펀지·퍼프·브러쉬) 언급, 블렌딩·도포 고민
- 헤어: 모발/두피 고민(손상·탈모·볼륨·두피 상태), 헤어 케어 루틴
- 이너뷰티: 피부·신체 내부 영양 고민, 섭취 루틴(앰플·영양제 복용)
- 향수/바디: 선호 향, 체취 고민, 바디 케어 루틴
- 네일: 손톱 고민(약함·갈라짐), 네일아트 취향, 네일 관리 루틴
- 생활용품/생활가전: 식기·주방·수납 용품 고민, 가전 사용 목적, 소재 선호

페르소나 필드 → 쿼리 매핑 기준

[니즈 필드 → user_need_query에 반영]
- 니즈 필드의 핵심 고민을 상품 기능/효과 용어로 변환하세요
- 사용자 표현(당김·푸석함 등)을 그대로 쓰지 말고 기능으로 치환하세요
  예: "피부가 속당긴다" → "피부 속 보습·수분 공급"
  예: "탄력이 떨어진다" → "피부 탄력 개선"
  예: "손톱이 잘 갈라진다" → "손톱 강화"
- 라이프스타일 필드의 환경·맥락에서 유추되는 기능 니즈를 보완하세요
  예: "실내 냉난방 환경" → 보습·수분 유지 니즈 추가
  예: "세안 직후 첫 단계로 흡수" → 토너/스킨 타입 신호

[취향 필드 → user_preference_query에 반영]
- 제형·맛·성분·가치관·포장 선호를 카테고리에 맞게 추출하세요
- 기피 표현은 "~프리" 형태로 변환하세요
  예: "비린 맛이 싫다" → "비린맛 프리"
  예: "향료 자극이 싫다" → "무향·무자극"
- 카테고리와 무관한 취향은 포함하지 마세요

[기본 정보 + 니즈 → persona_query에 반영]
- persona_query는 product_target_user와 의미적으로 매칭됩니다
- product_target_user는 직업이 아니라 "어떤 고민을 가진 어떤 라이프스타일의 사람"으로 작성되어 있습니다
- 따라서 직업명·직업 특성은 포함하지 말고, 고민 유형·루틴 성향·사용 목적 중심으로 작성하세요
  잘못된 예) "28세 헤어살롱 스타일리스트, 장시간 작업으로 피부가 건조한 사람"
  올바른 예) "건조한 환경에서 피부 탄력과 보습이 떨어져 속부터 이너케어 루틴을 만들고 싶은 사람"
- 성분·제형·기능 표현은 포함하지 마세요
- 30~50자 이내

카테고리별 추가 지침

[향수/바디]
- user_need_query: 취향 필드의 향 선호 + 바디 케어 목적

[네일]
- user_need_query: 니즈 필드의 손톱 고민 또는 취향 필드의 네일아트 취향 + 목적

[생활용품/생활가전]
- user_need_query: 니즈의 불편(위생·편의성·소재·용도·가전 기능) + 사용 목적
- user_preference_query: 취향의 소재 선호/기피 반영 (스테인리스·구리·유리 / BPA·플라스틱 기피)
- 피부 고민·스킨케어 효능·화장품 성분 관련 표현 사용 금지

[스킨케어]
- user_need_query: 니즈 필드의 피부 고민 + 라이프스타일에서 유추한 환경 반영
  - 라이프스타일에서 사용 시점·행동이 언급된 경우 제형 신호로 변환
    예: "가방에서 꺼내 화장 위에 덧바름" → 미스트 타입 신호
    예: "세안 직후 첫 단계로 두드려 흡수" → 토너/스킨 타입 신호

[색조/메이크업]
- user_need_query: 니즈 필드의 메이크업 고민(발색·커버력·지속력) 중심
- user_preference_query: 취향 필드의 퍼스널컬러·베이스 호수·선호 색상 반영

[메이크업 도구]
- user_need_query: 취향·라이프스타일에서 도포 방식·블렌딩 고민 추출
- 피부 흡수·보습·성분 관련 표현 사용 금지

[헤어]
- user_need_query: 니즈 필드의 두피/모발 고민 중심
- 얼굴 피부 고민 표현 사용 금지

   - 카테고리명(이너뷰티·스킨케어·헤어 등)을 포함하지 마세요
   - 섭취 방법·사용 방법·기간 등 방법론 표현은 제외하세요
     - 잘못된 예) "꾸준한 섭취로 피부 속 수분을 채워..." → "피부 속 수분 공급·피부결 개선"

[이너뷰티]
- user_need_query: 니즈 필드의 피부·신체 내부 고민을 섭취 기반 개선 기능으로 변환
  - 니즈에 탄력·보습·손톱·모발 등 복수 고민이 있으면 핵심 1~2개만 선택하세요
  - 증상 표현은 니즈에 있는 내용만 기능으로 치환하세요 — 없는 기능 추가 금지
    - "피부 당김·푸석함"만 있으면 → "피부 속 보습·수분 공급" (탄력 없이)
    - "피부 탄력 저하·당김"이 있으면 → "피부 탄력·보습 개선"
  - "영양 보충"처럼 광범위한 표현 대신 니즈에서 직접 유추한 기능(수분·탄력·모발 강화 등)을 사용하세요
- 외용(바르는) 표현 사용 금지
- user_preference_query: 제형(액상·젤리·정·파우더)·맛·기피 성분·패키지 관련 취향을 하나의 문장으로 작성하세요
  - 잘못된 예) "액상 타입 선호, 베리맛, 비린맛 프리, 포장 완성도 우수"
  - 올바른 예) "비린 맛 없이 바로 마실 수 있는 상큼한 액상 타입으로 간편하게 챙기고 패키지도 마음에 들어야 꾸준히 손이 간다"
- persona_query: 아래 두 축을 모두 포함하세요
  - 고민 축: 니즈에서 언급된 핵심 고민을 기능 표현으로 변환하여 포함하세요
    - 증상 표현(푸석함·당김·건조) 그대로 사용 금지 → 기능 표현(보습·수분 등)으로 변환
    - 잘못된 예) "피부가 건조·푸석해 매일 간편 섭취로 개선하려는 사람"
    - 올바른 예) "피부 속 수분이 부족해 매일 간편하게 섭취하며 꾸준히 채우려는 사람"
    - 니즈에 없는 고민은 추가하지 마세요
  - 루틴 축: 매일 간편하게 섭취하며 꾸준히 개선하려는 성향


작성 규칙

1. user_need_query
   - 니즈 필드의 핵심 고민을 "상품 기능/효과 용어"로 변환하여 작성
   - 형용사 수식어와 상품 설명체 표현 없이 자연스러운 문장으로 작성하세요
     - 잘못된 예) "손상된 모발에 풍성하고 탄탄한 볼륨을 살려주는 효과를 제공한다"
     - 올바른 예) "열 손상으로 끊어지는 모발의 단백질을 보충하고 두피 유수분 균형을 회복한다"
   - 페르소나 필드에 없는 기능과 고민은 추가하지 마세요 — 니즈에 명시된 내용만 기능으로 변환하세요
     - 잘못된 예) 니즈에 "당김·푸석함"만 있는데 "탄력 개선" 추가
   - 카테고리명(이너뷰티·스킨케어·헤어 등)을 포함하지 마세요
   - [이너뷰티] 상품일 경우 섭취 방법·사용 방법·기간 등 방법론 표현은 제외하세요
     - 잘못된 예) "꾸준한 섭취로 피부 속 수분을 채워..." → "피부 속 수분 공급·피부결 개선"
   - product_function_desc에 존재할 법한 표현만 사용하세요
     - 상품 기능에 없는 표현(윤기·광채·생기 등)은 사용하지 마세요
   - 카테고리와 무관한 고민 제외
   - retrieval_query에서 사용하는 "문제 표현"을 그대로 사용하지 마세요
   - 40~60자 이내

2. user_preference_query
   - 취향 필드에 명시된 내용만 작성하세요. 라이프스타일 필드에서 임의로 preference를 유추하지 마세요
     - 잘못된 예) 라이프스타일에 "가방에 넣어 다닌다"고 했을 때 → "휴대용" preference 추가 금지
   - 쉼표로 항목을 나열하지 말고, 하나의 연속된 자연어 문장으로 작성하세요
     - 잘못된 예) "워터리 제형, 히알루론산, 알코올프리, 소용량"
     - 잘못된 예) "알코올 없는 워터리 제형, 히알루론산 성분, 소용량 패키지" (여전히 나열)
     - 올바른 예) "알코올 없이 산뜻하게 흡수되는 워터리 타입이어야 민감한 피부에도 자극 없이 쓸 수 있다"
   - 기피 표현은 문장 안에서 "~없는", "~프리" 형태로 자연스럽게 포함하세요
   - 40~60자 이내

3. retrieval_query
   - "문제 상황(1개) + 핵심 기능(1~2개) + 카테고리" 중심으로 작성
   - 문제 표현과 기능 키워드를 합쳐 총 3~4개 이내로 제한하세요
     - 잘못된 예) "건조 트러블 각질 수분공급 장벽강화 진정 스킨케어" (7개 — 과잉)
     - 올바른 예) "건조한 피부 트러블 수분 진정 스킨케어" (4개)
   - 니즈·라이프스타일 필드의 문제 표현을 유지하되 기능 키워드 1~2개 포함
   - 기능 키워드는 니즈 필드에 있는 고민에서만 유추하세요 — 없는 기능 추가 금지
   - 기능 키워드는 광범위한 표현보다 니즈에서 직접 유추한 구체 기능을 사용하세요
   - user_need_query처럼 기능만 나열하지 말고, 문제 + 기능이 함께 드러나야 합니다
   - 검색 키워드 중심 표현 (조사 최소화), 사용 맥락 1개만 포함, preference 표현 금지

4. persona_query
   - 니즈 필드의 핵심 고민 + 라이프스타일에서 유추한 루틴화 동기·사용 목적으로 작성
   - product_target_user와 의미적으로 매칭되도록 작성
   - 직업명·직업 특성은 포함하지 마세요 (product_target_user는 직업 기준이 아님)
   - 라이프스타일의 특정 시간·장소(퇴근길·출근길·오후 4시 등 개인 일과)는 포함하지 마세요
     — product_target_user는 고민 유형·사용 성향 기준이지 특정 상황 기준이 아닙니다
   - 성분·제형·기능 표현은 포함하지 마세요
   - 스킨케어가 아닌 경우 "피부 타입" 표현 사용 금지
   - 니즈의 핵심 고민 축과 라이프스타일에서 유추한 루틴·사용 패턴 축이 모두 드러나야 합니다
   - 30~50자 이내

공통 주의사항
- 특정 브랜드나 상품명을 포함하지 마세요
- 판단된 카테고리와 맞지 않는 항목은 비중을 낮추거나 제외하세요

출력 형식 (JSON만 출력, 설명 없음)
{{
  "need": "",
  "preference": "",
  "retrieval": "",
  "persona": ""
}}

Persona:
{persona_str}
"""
    return prompt
