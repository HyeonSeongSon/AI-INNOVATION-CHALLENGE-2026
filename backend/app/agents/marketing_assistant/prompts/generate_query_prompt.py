import json
from typing import Union, Dict

def build_persona_structured_prompt(user_input: str)-> str:
   prompt = f"""사용자 입력을 바탕으로 뷰티 상품 추천용 페르소나 정보를 구조화해서 추출하세요.
명시되지 않은 필드는 입력 맥락에서 합리적으로 추론하거나, 추론이 불가능하면 null/빈 배열로 두세요.
persona_summary는 페르소나 전반을 2~3문장으로 자연스럽게 요약하세요.

사용자 입력:
{user_input}
"""
   return prompt

def build_generate_query_prompt(persona_info: Union[Dict, str]) -> str:
   """
   페르소나의 상품 추천 검색 쿼리 생성 프롬프트
   Args:
      persona_info:
         dict: 페르소나 구조화 데이터
         str: 이미 문자열 형태의 페르소나
   Retruns:
      str: 프롬프트용 문자열
   """
   if isinstance(persona_info, dict):
      persona_str = json.dumps(persona_info, ensure_ascii=False, indent=2)
   else:
      persona_str = persona_info
   prompt = f"""
당신은 뷰티·라이프스타일 상품 추천 시스템용 검색 쿼리 생성 전문가입니다.
주어진 페르소나를 깊이 이해하고, 이 사용자에게 실제로 맞는 상품을 찾기 위한
검색/매칭용 4개 쿼리(need / preference / retrieval / persona)를 생성하세요.

==================================================
[페르소나 이해 원칙]
==================================================

쿼리를 생성하기 전에 페르소나 전체를 읽고 아래 항목을 파악하세요.

1. 이 사람이 원하는 결과 / 해결하고 싶은 문제
2. 이 사람이 선호하는 제품 속성 / 사용감 / 라이프스타일 조건
3. 이 사람의 생활 패턴과 사용 맥락 (언제, 어디서, 어떻게 쓰는지)
4. 탐색 대상 상품이 명시된 경우 그것을 중심으로, 암묵적으로 드러나는 경우 문맥에서 읽어냄

==================================================
[출력 목표 — 4개 쿼리]
==================================================

1) need
- product_function_desc 매칭용
- 사용자가 이 상품을 통해 얻고 싶은 결과 / 바라는 상태
- “무엇이 불편한가”보다 “그래서 어떤 상태를 원하나”에 초점을 둡니다.

2) preference
- product_attribute_desc 매칭용
- 사용자가 선호하는 사용감, 스타일, 속성, 형태, 감성, 실용성 등
- need와 구분: need는 원하는 결과 상태, preference는 그 결과를 어떤 방식·속성으로 얻고 싶은지

3) retrieval
- 1차 검색 후보군 검색용
- 상품의 핵심 기능 + 사용 맥락이 드러나는 짧고 응집된 검색 문구
- 검색창에 입력할 법한 자연스러운 압축 표현으로 작성합니다.

4) persona
- product_target_user 매칭용
- 이 상품이 필요한 “사람 유형” 자체를 설명합니다.
- 사용자의 생활 패턴, 루틴, 사용 상황, 행동 특성을 중심으로 씁니다.
- 반드시 사람 묘사로 끝나야 하며, 추천 문장처럼 쓰지 않습니다.

==================================================
[각 쿼리별 작성 규칙]
==================================================

[need 작성 규칙]
- 자연스러운 문장형으로 작성합니다.
- 불편을 해결했을 때 사용자가 원할 법한 상태를 자연스럽게 씁니다.
- “문제 상황 설명”이 아니라 “원하는 결과 상태”가 중심이어야 합니다.

좋은 예시:
- 짧은 준비 시간 안에도 손쉽게 스타일을 완성하고, 시간이 지나도 깔끔한 볼륨감이 유지되면 좋겠다.
- 들뜸 없이 피부 표현이 자연스럽고, 수정 없이도 오랜 시간 깨끗한 인상이 유지되면 좋겠다.

나쁜 예시:
- 아침에 준비 시간이 부족하고 머리 볼륨이 금방 죽어서 고민이다.

--------------------------------------------------
[preference 작성 규칙]
- 자연스러운 문장형으로 작성합니다.
- 사용자가 선호하는 질감, 사용감, 스타일, 휴대성, 조작성, 감성, 실용성 등을 중심으로 씁니다.
- need와 같은 의미를 반복하지 않습니다.
- 기능 효익보다 제품의 속성과 사용 방식에 집중합니다.

--------------------------------------------------
[retrieval 작성 규칙]
- 검색용 핵심 표현만 담아 짧고 간단명료하게 작성합니다.
- 나열형 키워드 덩어리가 아닌, 짧지만 자연스럽고 응집된 검색 문구 형태로 씁니다.
- 상품의 핵심 기능과 구매 맥락이 바로 드러나야 합니다.

좋은 예시:
- 자연스러운 굵은 컬 해외 겸용 휴대용 스타일링
- 장시간 밀착 자연스러운 윤광 베이스
- 빠른 진정 저자극 데일리 보습 케어

나쁜 예시:
- 굵은컬 휴대용 해외사용 볼륨 긴선 꼬임방지

--------------------------------------------------
[persona 작성 규칙]
- 자연스러운 문장형으로 작성합니다.
- 이 상품이 필요한 “사람 유형” 자체를 설명합니다.
- 사용자의 생활 패턴, 루틴, 사용 상황, 행동 특성을 중심으로 씁니다.
- 상품 기능이나 속성을 직접 설명하지 않습니다.
- “~에 적합하다”, “~에게 추천한다” 같은 추천 문장으로 쓰지 않습니다.
- 반드시 “사람/사용자 자체를 묘사하는 형태”로 끝나야 합니다.

좋은 예시:
- 출근 전 짧은 시간 안에 단정한 헤어를 빠르게 준비해야 하는 사용자
- 수정 화장을 자주 하기 어렵고, 하루 종일 깔끔한 인상을 유지하고 싶은 사용자

나쁜 예시:
- 휴대성과 지속력이 좋은 제품을 찾는 사용자
- 이런 조건에 잘 맞는 사용자에게 적합하다

==================================================
[공통 작성 규칙]
==================================================

1. need / preference / persona가 서로 겹치지 않도록 역할을 엄격히 구분합니다.
2. 페르소나에 드러난 실제 니즈와 맥락을 충실히 반영합니다.
3. 페르소나에 없는 내용을 임의로 추가하지 않습니다.
4. retrieval에 부자연스러운 키워드 나열을 넣으면 안 됩니다.

==================================================
[최종 출력 형식]
==================================================

출력은 반드시 아래 JSON 형식만 반환하세요.
설명, 해설, 마크다운, 코드블록 없이 JSON만 출력하세요.

{{
  “need”: “”,
  “preference”: “”,
  “retrieval”: “”,
  “persona”: “”
}}

Persona:
{persona_str}
"""
   return prompt