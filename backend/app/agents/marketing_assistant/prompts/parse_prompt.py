def build_crm_message_parse_prompt() -> str:
    """
    사용자 요청에서 product_id와 purpose를 파싱하는 프롬프트.
    여러 상품이 언급된 경우 각각 별도 task로 분리.
    """
    return """CRM 메시지 생성 요청을 파싱하여 JSON으로 반환하세요.

**출력 필드:** tasks(리스트) — 각 항목: product_id(단일), purpose(단일/null)

**규칙:**
- 상품이 여러 개 언급된 경우 각 상품마다 별도 task로 분리
- 하나의 purpose가 여러 상품에 공통 적용되는 경우 각 task에 동일한 purpose 설정
- 상품마다 다른 purpose가 명시된 경우 각각의 purpose 적용

**목적(purpose) 매핑 - 절대 규칙:**
**경고: purpose 필드는 반드시 다음 7개 값 중 정확히 하나만 사용해야 합니다. 다른 값은 절대 사용 불가합니다.**

허용된 값 및 키워드 매핑:

1. "브랜드/제품 첫소개" (기본값)
   - 키워드: 소개, 광고, 홍보, 추천, 안내 (다른 특정 목적이 없을 때)
   - 예: "제품홍보", "브랜드소개", "광고메시지", "추천메시지"

2. "신제품 홍보"
   - 키워드: 신제품, 신상품, 새로운, NEW, 런칭, 출시, 리뉴얼
   - 예: "신제품홍보", "신상품소개", "NEW 출시", "런칭메시지"

3. "베스트셀러 제품 소개"
   - 키워드: 베스트셀러, 베스트, 인기제품, 스테디셀러, 판매1위, 인기상품
   - 예: "베스트상품", "인기제품소개", "판매1위추천"

4. "프로모션/이벤트 소개"
   - 키워드: 프로모션, 이벤트, 할인, 특가, 세일, 행사, 기획전
   - 예: "프로모션소개", "이벤트메시지", "할인안내", "특가홍보", "세일광고"

5. "성분/효능 강조 소개"
   - 키워드: 성분, 효능, 효과, 기능, 레티놀, 히알루론산, 나이아신아마이드 등 성분명
   - 예: "성분강조", "효능중심", "히알루론산제품"

6. "피부타입/고민 강조 소개"
   - 키워드: 건성, 지성, 복합성, 민감성, 여드름, 주름, 미백, 트러블, 모공
   - 예: "건성피부용", "여드름케어", "미백제품"

7. "라이프스타일/연령대 강조 소개"
   - 키워드: 20대, 30대, 40대, 직장인, 학생, 주부, 바쁜, 간편한
   - 예: "30대직장인용", "바쁜아침", "학생추천"

**예시:**
입력: "p001 상품으로 프로모션 메시지 작성해줘"
출력: {"tasks": [{"product_id": "p001", "purpose": "프로모션/이벤트 소개"}]}

입력: "p001, p002 상품으로 프로모션 메시지 작성해줘"
출력: {"tasks": [{"product_id": "p001", "purpose": "프로모션/이벤트 소개"}, {"product_id": "p002", "purpose": "프로모션/이벤트 소개"}]}

입력: "p001 상품으로 성분강조 메시지를, p002 상품으로 베스트셀러 홍보 메시지를 작성해줘"
출력: {"tasks": [{"product_id": "p001", "purpose": "성분/효능 강조 소개"}, {"product_id": "p002", "purpose": "베스트셀러 제품 소개"}]}
"""


