def get_skincare_hint(tag: str, subtag: str) -> str:
    """태그·서브태그 조합으로 스킨케어 서브타입 힌트 반환. 없으면 태그 → 기본 순으로 fallback."""
    hint = _SKINCARE_SUBTYPE_HINTS.get((tag, subtag))
    if hint:
        return hint
    # 태그 레벨 fallback: 같은 태그 내 첫 번째 힌트
    for (t, _), h in _SKINCARE_SUBTYPE_HINTS.items():
        if t == tag:
            return h
    # 최종 fallback: 기존 기본 힌트
    return _CATEGORY_HINTS.get("skincare", "")



