"""역방향 합성 데이터 생성용 프롬프트"""
import json
import random
from prompt_hint import get_category_hint



def build_reverse_persona_prompt(product: dict) -> str:
    """
    상품 정보를 받아 해당 상품을 원할 법한 페르소나를 생성하는 프롬프트 반환
    """
    s = product.get("structured", {})
    category = product.get("_source_category", "skincare")
    tag = product.get("태그", "")
    subtag = product.get("서브태그", "")
    category_hint = get_category_hint(category)


    product_context = {
        "카테고리": product.get("카테고리", ""),
        "태그": tag,
        "서브태그": subtag,
        **s,
    }
    product_json = json.dumps(product_context, ensure_ascii=False, indent=2)

    # 다양성 유도: 나이대·직업·수면 힌트를 랜덤으로 제공
    age_hint = random.choice([
        "20대 초중반(21~25세)", "20대 후반(26~29세)", "30대 초반(30~34세)",
        "30대 후반(35~39세)", "40대 초반(40~44세)", "40대 후반(45~49세)", "50대 초반(50~55세)"
    ])
    job_hint = random.choice([
        "사무직(금융·법률·행정)", "전문직(의료·교육·연구)", "크리에이티브직(디자인·마케팅·미디어)",
        "서비스직(뷰티·요식·유통)", "프리랜서·1인사업자", "육아중인 주부·부모",
        "IT·개발·데이터직", "외근·영업직"
    ])
    sleep_hint = random.choice(["4시간", "5시간", "6시간",  "7시간",  "8시간", "9시간"])


    return f"""당신은 뷰티 상품 추천 시스템의 평가 데이터셋을 만드는 전문가입니다.
아래 상품 정보를 보고, 이 상품을 자연스럽게 추천받을 법한 실제 사용자 페르소나를 생성하세요.

[상품 정보]
{product_json}

[카테고리 특화 지침]
{category_hint}

[필드 정의]
- 기본 정보: 나이(정수), 성별, 직업 — 직업은 이후 라이프스타일·니즈와 인과 관계를 가져야 합니다
- 라이프스타일: 직업과 생활 리듬에서 비롯된 구체적인 일과 패턴, 습관, 공간
- 니즈: 이 상품을 찾게 된 핵심 고민 — 상품 카테고리 특성에 맞는 시간대와 해결 방식으로 표현
- 취향: 상품 선택에 영향을 주는 형태·맛·성분·가격·포장 선호

[생성 규칙]
1. target_user 설명을 최우선 참고하여 페르소나의 특성을 설정하세요
2. 페르소나는 구체적이고 현실감 있어야 합니다 (이 상품의 광고 모델이 아닌, 실제 구매자처럼)
3. 니즈·취향 필드에는 라이프스타일 필드와 중복되는 배경·상황 설명을 쓰지 마세요. 고민과 선호를 직접 서술하세요
4. 상품이 해결하는 핵심 고민을 니즈에 자연스럽게 녹여내세요. 단, 카테고리 특화 지침에 별도 지시가 있으면 그것을 우선합니다
5. 각 필드는 1~2문장의 자연어 문장으로 작성하세요 (단어 나열 금지, 세미콜론(;)으로 문장을 연결하지 말고 마침표로 끊어 쓰세요)
6. 다양성을 위해 아래 힌트를 반영하세요
   - 나이대 힌트: {age_hint} 범위 내에서 설정
   - 직업 힌트: {job_hint} 계열에서 구체적인 직업을 설정하고, 해당 직업의 특성이 "라이프스타일"뿐 아니라 "니즈의 원인"과도 자연스럽게 연결되어야 합니다
   - 생활 리듬 힌트: 수면시간 {sleep_hint}을 직접적으로 언급하지 말고,
     "하루 일과 패턴", "활동 시간대", "피로 누적 상황", "생활 습관" 등의 형태로 자연스럽게 드러나도록 표현하세요
   - 가격대: 카테고리와 상품 가격대를 고려해 현실적으로 작성
7. 나이는 정수, 성별은 "남성"/"여성" 중 하나로만 작성하세요
8. 니즈 트리거는 직업 특성에서 도출하세요. 시간대("오후 4시")나 신체 증상을 고정 패턴으로 쓰지 말고,
   직업에서 자연스럽게 비롯되는 상황으로 표현하세요
   - 예) 사진 작가 → "조명 아래 장시간 있다 보면 피부가 건조해지고 결이 거칠어지는 게 카메라에 잡혀 신경 쓰인다"
   - 예) 육아 중인 부모 → "아이 목욕을 매일 시키다 보니 손이 거칠어지고 손톱이 자꾸 갈라진다"
9. 상품 문서의 표현을 그대로 쓰지 말고, 소비자가 일상에서 느끼는 감각이나 경험의 언어로 재표현하세요
   - 잘못된 예) "히알루론산·세라마이드 함유 성분 선호" → 올바른 예) "바른 다음 당기지 않아야 저녁에도 편하게 쓸 수 있다"
   - 효과 기대도 마케팅 언어가 아닌 소비자 언어로 쓰세요
   - 잘못된 예) "꾸준한 사용으로 피부 개선 기대" → 올바른 예) "2주쯤 쓰다 보면 아침 피부 상태가 조금이라도 달라지면 된다"
10. 취향 필드는 "[스펙명] 선호" 형태로 쓰지 말고, 이 사람의 상황이나 성격 때문에 그 속성이 왜 중요한지를 서술하세요
    - 잘못된 예) "가벼운 워터리 제형과 무향 성분 선호"
    - 올바른 예) "향이 강하면 아침마다 쓰기가 꺼려지는 편이라 향이 없거나 은은해야 매일 쓸 수 있다"
11. 다른 카테고리와 오인될 수 있는 표현은 쓰지 마세요
    - 상품 카테고리 종류: [스킨케어, 색조, 향수/바디, 헤어, 이너뷰티, 뷰티툴, 생활도구]
    - 예) 이너뷰티 작성 시 "피부에 바르면", "흡수되는 느낌" 같은 외용 표현 금지
12. 각 필드에 중복된 내용은 작성하지 마세요
13. 각 필드에 포인트를 과하게 넣지 마세요. 상품의 핵심 고민 1~2개만 니즈에 반영하세요
    - 잘못된 예) 니즈에 건조함·트러블·모공·주름을 모두 포함 → 핵심 1~2개만


[출력 형식 - JSON만 출력, 설명 없음]
{{
  "기본 정보": "나이(정수), 성별, 직업",
  "라이프스타일": "직업·생활 리듬에서 비롯된 일과 패턴과 습관",
  "니즈": "이 상품을 찾게 된 핵심 고민",
  "취향": "형태·맛·성분·가격·포장 등 상품 선택 기준"
}}"""
