def build_daily_goods_product_prompt(extra_category: str, product_document: str, category_list: List[str]) -> str:
    extra_prompts = {
        "home_appliance": _build_home_appliance_extra_prompt,
        "tableware": _build_tableware_extra_prompt,
    }
    extra_prompt = extra_prompts[extra_category]()
    base_prompt = build_generate_base_product_document_prompt(product_document, category_list)
    return base_prompt + extra_prompt
